# Getting the data type of the output

print(type(2 + 3))   # addition(+)
print(type(3 - 1))   # subtraction(-)
print(type(2 * 3))   # multiplication(*)
print(type(3 / 2))   # division(/)
print(type(3 ** 2))  # exponential(**)
print(type(3 % 2))   # modulus(%)
print(type(3 // 2))  # Floor division operator(//)
